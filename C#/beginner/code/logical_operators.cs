using System;


class Code
{
    static void Main(string[] args)
    {
        // && AND
        // || OR

        int x = 10;
        int y = 5;

        if(x >= 10 && y >= 5);
        {
            Console.WriteLine("x is greater or equal to 10 and y is greater or equal to 5");
        }
        if(x >= 7 || y>=7)
        {
            Console.WriteLine("x or y is greater or equal to 7");
        }
    }
}